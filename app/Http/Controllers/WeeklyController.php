    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, [
            'amount' => 'required'
        ]);

        $transaction = Transaction::where('member_id', $request->member_id)->where('is_closed', 0)->first();


        $pool = '0123456789abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ';
        $rand = substr(str_shuffle(str_repeat($pool, 5)), 0, 10);

        $data = [
            'transaction_id' => $transaction->id,
            'ornumber' => $rand.$transaction->member_id,
            'member_id' => $transaction->member_id,
            'amount' =>  (-1 * $request->amount),
            'tag' => 'weekly'
        ];

        PaidBill::create($data);

        return redirect()->route('weekly.index')->with('message', 'Weekly Created Successfully!');
    }

